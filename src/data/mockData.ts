export const generateTripPlan = (
  cityId: string,
  spotIds: string[],
  travelModeId: string
): TripPlan => {
  const travelMode = TRAVEL_MODES.find(mode => mode.id === travelModeId) || TRAVEL_MODES[0];
  
  const allSpots = getTouristSpotsByCity(cityId);
  
  const selectedSpots = allSpots.filter(spot => spotIds.includes(spot.id));
  
  const orderedSpots = selectedSpots.map((spot, index) => {
    let distanceToNext = 0;
    let timeToNext = 0;
    let transportationCostToNext = 0;
    
    if (index < selectedSpots.length - 1) {
      const nextSpot = selectedSpots[index + 1];
      distanceToNext = Number(((Math.random() * 5) + 1).toFixed(1));
      timeToNext = Math.round((distanceToNext / travelMode.speedKmPerHour) * 60);
      transportationCostToNext = Math.round(distanceToNext * travelMode.costPerKm);
    }
    
    const hourOfDay = 9 + (index * 2);
    const hour = hourOfDay % 12 === 0 ? 12 : hourOfDay % 12;
    const period = hourOfDay < 12 ? "AM" : "PM";
    
    return {
      ...spot,
      visitOrder: index + 1,
      recommendedTimeToVisit: `${hour}:00 ${period}`,
      timeToNextSpot: timeToNext,
      distanceToNextSpot: distanceToNext,
      transportationCostToNext: transportationCostToNext,
    } as TripSpot;
  });
  
  const ticketsCost = orderedSpots.reduce((sum, spot) => sum + spot.ticketPrice, 0);
  const transportationCost = orderedSpots.reduce((sum, spot) => sum + spot.transportationCostToNext, 0);
  const totalCost = ticketsCost + transportationCost;
  
  const totalVisitHours = orderedSpots.reduce((sum, spot) => sum + spot.averageTimeSpent, 0);
  const totalTravelHours = orderedSpots.reduce(
    (sum, spot) => sum + (spot.timeToNextSpot / 60), 0
  );
  const totalDuration = totalVisitHours + totalTravelHours;
  
  return {
    id: `trip-${cityId}-${Date.now()}`,
    cityId,
    spots: orderedSpots,
    totalCost: {
      tickets: ticketsCost,
      transportation: transportationCost,
      total: totalCost,
    },
    totalDuration,
    selectedTravelMode: travelMode,
  };
};
